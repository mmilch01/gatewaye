    private void initial_apply_buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_initial_apply_buttonMousePressed
        if (!ensure_all_there()) {
            GUIUtils.warn("The following errors were found: \n" + error, "Add error");
            error = null;
        } else {
            try {
                this.p = fill_in_properties();
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchPaddingException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidKeyException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidAlgorithmParameterException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalBlockSizeException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BadPaddingException ex) {
                Logger.getLogger(InitialSetupGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                controller.initialSetupDone(p);
                this.dispose();
            } catch (IOException ex) {
                GUIUtils.warn(ex.toString(), "Setup error");
                System.exit(1);
            }
        }
    }//GEN-LAST:event_initial_apply_buttonMousePressed

